EraSystem = {} do


    local eraRank = {
        [Eras.WW2] = 1,
        [Eras.EARLYCOLDWAR] = 2,
        [Eras.LATECOLDWAR] = 3,
        [Eras.MODERN] = 4,
    }

    local EraNameMEStrings = {
        [Eras.WW2] = "WW2",
        [Eras.EARLYCOLDWAR] = "EARLYCOLDWAR",
        [Eras.LATECOLDWAR] = "LATECOLDWAR",
        [Eras.MODERN] = "MODERN",
    }


    local logisticsTasks = {
        [AITaskTypes.CAPTURE_HELO] = true,
        [AITaskTypes.REINFORCEMENT_HELO] = true,
        [AITaskTypes.RESUPPLY_CARGO] = true,
    }
    ---@return string[] eras array of selected Eras.* values
    function EraSystem.getSelectedEras()
        local selected = Config.era_system.eras_selected
        if type(selected) ~= "table" or #selected == 0 then
            return { Eras.MODERN }
        end
        -- Return a defensive copy so callers cannot mutate Config.
        local out = {}
        for _, era in ipairs(selected) do
            out[#out + 1] = era
        end
        return out
    end

    ---@return table<string, boolean> set keyed by DCS type string
    function EraSystem.getEraAircraft()
        local set = {}
        for _, era in ipairs(EraSystem.getSelectedEras()) do
            local list = Stocks.AircraftEras[era]
            if list then
                for _, dcs_type in ipairs(list) do
                    set[dcs_type] = true
                end
            end
        end
        return set
    end

    local acft_err_warned = false
    ---@return table<string, boolean> set keyed by DCS type string
    function EraSystem.getEnabledAircraft()
        local era_aircraft = EraSystem.getEraAircraft()
        local enabled = Config.era_system.enabled_aircraft
        if type(enabled) ~= "table" or #enabled == 0 then
            return era_aircraft
        end
        local set = {}
        for _, aircraft in ipairs(enabled) do
            if era_aircraft[aircraft] then
                set[aircraft] = true
            elseif not acft_err_warned then
                acft_err_warned = true
                env.error("CONFIG ERROR: Enabled aircraft '" .. tostring(aircraft)
                    .. "' is not valid in the selected era(s); dropping.",true)
            end
        end
        return set
    end

    ---@param dcs_type_string string DCS aircraft type string
    ---@return boolean enabled
    function EraSystem.isAircraftEnabled(dcs_type_string)
        if not dcs_type_string then return false end
        return EraSystem.getEnabledAircraft()[dcs_type_string] == true
    end

    ---@return table<string, boolean> set keyed by weapon clsid
    local function getRestrictedWeapons()
        local set = {}
        local cfg = Config and Config.era_system
        local restricted = cfg and cfg.restricted_weapons
        if type(restricted) == "table" then
            for _, clsid in ipairs(restricted) do
                set[clsid] = true
            end
        end
        return set
    end

    ---@param clsid string weapon clsid
    ---@return boolean
    local function isWeaponEraValid(clsid)
        local rec = Stocks.EquipmentData[clsid]
        local eras = (rec and rec.eras) or { Eras.MODERN }
        if type(eras) ~= "table" then
            eras = { eras }
        end
        for _, selected_era in ipairs(EraSystem.getSelectedEras()) do
            for _, w_era in ipairs(eras) do
                if w_era == selected_era then
                    return true
                end
            end
        end
        return false
    end

    -- for caching
    local enabled_weapons
    --- Set of weapons that are (a) usable by at least one enabled aircraft
    --- (b) not restricted (c) era-appropriate.
    ---@return table<string, boolean> set keyed by weapon clsid
    function EraSystem.getEnabledWeapons()
        if enabled_weapons then return enabled_weapons end
        local enabled_aircraft = EraSystem.getEnabledAircraft()
        local restricted = getRestrictedWeapons()
        local set = {}
        local any_mapping = false

        local enabled = Config and Config.era_system and Config.era_system.enabled_aircraft
        local has_user_aircraft = (type(enabled) == "table" and #enabled > 0)

        if has_user_aircraft then
            for aircraft in pairs(enabled_aircraft) do
                local weapons = Stocks.AircraftLoads[aircraft]
                if weapons then
                    any_mapping = true
                    for _, clsid in ipairs(weapons) do
                        if not restricted[clsid] and isWeaponEraValid(clsid) then
                            set[clsid] = true
                        end
                    end
                end
            end
        end

        if not any_mapping then
            for _, clsid in pairs(Stocks.Equipment) do
                if not restricted[clsid] and isWeaponEraValid(clsid) then
                    set[clsid] = true
                end
            end
        end

        if not enabled_weapons then
            enabled_weapons = set
        end
        return set
    end

    ---@param weapon_clsid string weapon clsid
    ---@return boolean enabled
    function EraSystem.isWeaponEnabled(weapon_clsid)
        if not weapon_clsid then return false end
        if not enabled_weapons then
            enabled_weapons = EraSystem.getEnabledWeapons()
        end
        return enabled_weapons[weapon_clsid] == true
    end

    --- Check whether an AI task type is allowed for the selected era(s).
    ---@param ai_task_type string value from AITaskTypes.*
    ---@return boolean allowed
    function EraSystem.isTaskTypeAllowed(ai_task_type)

        if not ai_task_type then return false end
        if logisticsTasks[ai_task_type] then return true end

        local blacklist = {
            [Eras.WW2] = {
                AITaskTypes.TANKER,
                AITaskTypes.SEAD,
                AITaskTypes.AWACS,
                AITaskTypes.JTAC,
                AITaskTypes.INTERCEPT,
            },
            [Eras.EARLYCOLDWAR] = {
                AITaskTypes.SEAD,
                AITaskTypes.AWACS,
                AITaskTypes.JTAC,
                AITaskTypes.INTERCEPT,
            },
            [Eras.LATECOLDWAR] = {},
            [Eras.MODERN] = {},
        }

        for _, era in ipairs(EraSystem.getSelectedEras()) do
            -- Collect this era's banned tasks into a quick lookup set.
            local banned = false
            local era_list = blacklist and blacklist[era]
            if era_list then
                for _, banned_task in ipairs(era_list) do
                    if banned_task == ai_task_type then
                        banned = true
                        break
                    end
                end
            end
            -- Permitted in this era (not banned) => allowed via union.
            if not banned then
                return true
            end
        end
        return false
    end

    ---@param zone_type ZoneTypes
    ---@return boolean
    function EraSystem.isZoneTypeAllowed(zone_type)
        if not zone_type then return false end
        if zone_type == ZoneTypes.AIRBASE then return true end
        local blacklist = Config.era_system.disable_zone_types_for_era
        if not blacklist then return true end
        for _, era in ipairs(EraSystem.getSelectedEras()) do
            local banned = blacklist[era]
            if banned then
                for _, bt in ipairs(banned) do
                    if bt == zone_type then return false end
                end
            end
        end
        return true
    end

    ---@return Eras
    function EraSystem.getActiveEra()
        local selected = EraSystem.getSelectedEras()
        return selected[#selected] or Eras.MODERN
    end

    ---@return number rank
    local function maxSelectedEraRank()
        local best = 0
        for _, era in ipairs(EraSystem.getSelectedEras()) do
            local r = eraRank[era] or 0
            if r > best then best = r end
        end
        return best
    end

    ---@return boolean
    function EraSystem.isHelicopterEraCapable()
        return maxSelectedEraRank() >= (eraRank[Eras.EARLYCOLDWAR] or 2)
    end

    ---@return boolean
    function EraSystem.isAirResupplyEraCapable()
        return maxSelectedEraRank() >= (eraRank[Eras.EARLYCOLDWAR] or 2)
    end

    ---@return boolean
    function EraSystem.isBoomTankerEraCapable()
        return maxSelectedEraRank() >= (eraRank[Eras.LATECOLDWAR] or 3)
    end

    ---@param base_name table|nil per-era table or base template name "<SIDE> <TASK>"
    ---@return string|nil --era-specific name, or base_name unchanged
    function EraSystem.resolveTaskTemplateName(base_name)
        if not base_name then return nil end
        local era_string = EraNameMEStrings[EraSystem.getActiveEra()]

        if era_string and base_name[era_string] then return base_name[era_string] end


        -- Active era unfilled: fall back to MODERN, then any present entry.
        local modern_token = EraNameMEStrings[Eras.MODERN]
        if modern_token and base_name[modern_token] then return base_name[modern_token] end
        for _, name in pairs(base_name) do
            if type(name) == "string" then return name end
        end
        return nil
    end

    ---@param base_name table|nil per-era table
    ---@return string|nil group name for the active era, or nil if no own entry
    function EraSystem.resolveTaskTemplateNameStrict(base_name)
        if type(base_name) ~= "table" then return nil end
        local era_string = EraNameMEStrings[EraSystem.getActiveEra()]
        if era_string and base_name[era_string] then return base_name[era_string] end
        return nil
    end

    ---@param entry table Stocks.GroundUnits.*
    ---@return boolean
    local function entryFitsSelectedEras(entry)
        local minR = eraRank[entry.era_min] or 1
        local maxR = eraRank[entry.era_max] or eraRank[Eras.MODERN]
        for _, era in ipairs(EraSystem.getSelectedEras()) do
            local r = eraRank[era]
            if r and r >= minR and r <= maxR then
                return true
            end
        end
        return false
    end


    ---@param filters table|nil {
    ---   role=string|nil,                 -- exact role match
    ---   era=string|nil,                  -- a single Eras.* the entry must cover;
    ---                                    --   when nil, the entry must fit ANY selected era
    ---   country=number|nil,              -- must appear in entry.countries
    ---   sam_classification=string|nil,   -- exact SAM_TYPES.* match
    --- }
    ---@return table[] array of matching database entries
    function EraSystem.getGroundUnits(filters)
        filters = filters or {}
        local out = {}
        for _, entry in pairs(Stocks.GroundUnits) do
            local ok = true

            if filters.role and entry.role ~= filters.role then ok = false end

            if ok and filters.sam_classification
               and entry.sam_classification ~= filters.sam_classification then
                ok = false
            end

            if ok and filters.country then
                local found = false
                for _, c in ipairs(entry.countries or {}) do
                    if c == filters.country then found = true break end
                end
                if not found then ok = false end
            end

            if ok then
                if filters.era then
                    local min_r = eraRank[entry.era_min] or 1
                    local max_r = eraRank[entry.era_max] or eraRank[Eras.MODERN]
                    local r = eraRank[filters.era]
                    if not (r and r >= min_r and r <= max_r) then ok = false end
                elseif not entryFitsSelectedEras(entry) then
                    ok = false
                end
            end

            if ok then out[#out + 1] = entry end
        end
        return out
    end

